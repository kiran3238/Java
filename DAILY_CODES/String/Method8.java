//INDEXOF.........
//Parameter:  Character(ch to find character),integer(index to start searching)
//Return Type:  Integer
//
class IndexOfDemo{

	public static void main(String[]args){

		String str1="Shashi";


		System.out.println(str1.indexOf('h',0));//1
		System.out.println(str1.indexOf('h',2));//4
		System.out.println(str1.indexOf('s',0));//3
		System.out.println(str1.indexOf('a',0));//2
	}

}

